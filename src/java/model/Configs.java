public class Configs {
    private String id, tenweb, facebook, mail, phone, diachiduong,instagram, diachithanhpho,zalo;

    public Configs() {
    }

    public Configs(String id, String tenweb, String facebook, String mail, String phone, String diachiduong, String instagram, String diachithanhpho, String zalo) {
        this.id = id;
        this.tenweb = tenweb;
        this.facebook = facebook;
        this.mail = mail;
        this.phone = phone;
        this.diachiduong = diachiduong;
        this.instagram = instagram;
        this.diachithanhpho = diachithanhpho;
        this.zalo = zalo;
    }

    
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTenweb() {
        return tenweb;
    }

    public void setTenweb(String tenweb) {
        this.tenweb = tenweb;
    }

    public String getFacebook() {
        return facebook;
    }

    public void setFacebook(String facebook) {
        this.facebook = facebook;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getDiachiduong() {
        return diachiduong;
    }

    public void setDiachiduong(String diachiduong) {
        this.diachiduong = diachiduong;
    }

    public String getInstagram() {
        return instagram;
    }

    public void setInstagram(String instagram) {
        this.instagram = instagram;
    }

    public String getDiachithanhpho() {
        return diachithanhpho;
    }

    public void setDiachithanhpho(String diachithanhpho) {
        this.diachithanhpho = diachithanhpho;
    }

    public String getZalo() {
        return zalo;
    }

    public void setZalo(String zalo) {
        this.zalo = zalo;
    }


    
}
